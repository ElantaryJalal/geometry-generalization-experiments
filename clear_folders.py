#!/usr/bin/env python3
"""
Clears all output folders and files generated by the geometry & generalisation experiment script.
"""

import shutil
import os
import glob

FOLDERS = [
    "Subtraction_Encoders",
    "Addition_Encoders",
]

FILE_PATTERNS = [
    "encoder_comparison_subtraction_final_run*.png",
    "encoder_comparison_addition_final_run*.png",
]

def clear_all():
    removed = []

    for folder in FOLDERS:
        if os.path.exists(folder):
            shutil.rmtree(folder)
            removed.append(f"[DIR]  {folder}/")
        else:
            print(f"Skipped (not found): {folder}/")

    for pattern in FILE_PATTERNS:
        for f in glob.glob(pattern):
            os.remove(f)
            removed.append(f"[FILE] {f}")

    if removed:
        print("Removed:")
        for r in removed:
            print(f"  {r}")
        print(f"\nDone. {len(removed)} item(s) deleted.")
    else:
        print("Nothing to remove — all folders/files already clean.")

if __name__ == "__main__":
    clear_all()
